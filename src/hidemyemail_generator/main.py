async def _whoami(cookie_file: str, region: str = DEFAULT_REGION) -> bool:
    console = Console()
    if not os.path.exists(cookie_file):
        console.log(f'[bold red][ERR][/] No "{cookie_file}" file found')
        return False

    account = await fetch_account_info(cookie_file, region)
    if "error" in account:
        console.log(f"[bold red][ERR][/] {account['error']}")
        return False

    ds_info = account.get("dsInfo", {})
    full_name = ds_info.get("fullName") or " ".join(
        part for part in [ds_info.get("firstName"), ds_info.get("lastName")] if part
    )
    webservices = account.get("webservices", {})
    maildomain = webservices.get("maildomainws", {})

    table = Table(title="Current iCloud Cookie / 当前 iCloud Cookie", show_header=False)
    table.add_column("Field / 字段")
    table.add_column("Value / 值")
    table.add_row("Apple ID", ds_info.get("appleId") or "Unknown")
    table.add_row("Name / 名称", full_name or "Unknown")
    table.add_row("DSID", str(ds_info.get("dsid") or "Unknown"))
    table.add_row(
        "Hide My Email / 隐藏邮箱",
        "Available"
        if ds_info.get("isHideMyEmailFeatureAvailable")
        else "Unavailable or unknown",
    )
    table.add_row("User Partition / 用户分区", str(account.get("userPartition") or "Unknown"))
    table.add_row(
        "Maildomain",
        account.get("detectedMaildomainHost")
        or maildomain.get("url")
        or "Default",
    )
    console.print(table)
    return True


def _print_messages(db_file: str, only_codes: bool, limit: int) -> None:
    conn = connect_db(db_file)
    try:
        rows = list_messages(conn, only_codes=only_codes, limit=limit)
    finally:
        conn.close()

    table = Table(title="Verification Codes / 验证码" if only_codes else "Inbox Messages / 收件箱")
    table.add_column("Received / 收件时间")
    table.add_column("HME / 隐藏邮箱")
    table.add_column("Sender / 发件人")
    table.add_column("Subject / 标题")
    table.add_column("Code / 验证码")
    for row in rows:
        table.add_row(
            row["received_at"] or "",
            row["hme_address"] or "",
            row["sender"] or "",
            row["subject"] or "",
            row["code"] or "",
        )
    Console().print(table)


async def _sync_hme_to_db(
    cookie_file: str, region: str = DEFAULT_REGION, db_file: str = DEFAULT_DB_FILE
) -> int:
    async with RichHideMyEmail(cookie_file=cookie_file, region=region) as hme:
        result = await hme.list_email()

    if not result or not result.get("success"):
        error = result.get("error", {}) if result else {}
        if isinstance(error, dict):
            reason = error.get("errorMessage") or str(error)
        else:
            reason = result.get("reason", "Unknown") if result else "Unknown"
        raise click.ClickException(f"Failed to sync Hide My Email addresses: {reason}")

    conn = connect_db(db_file)
    try:
        count = 0
        for row in result.get("result", {}).get("hmeEmails", []):
            email = row.get("hme")
            if not email:
                continue
            upsert_address(
                conn,
                email,
                label=row.get("label") or "",
                state="unused" if row.get("isActive") else "trash",
                source="icloud",
                note=f"iCloud active={row.get('isActive')}",
            )
            count += 1
        return count
    finally:
        conn.close()


async def _capture_cookie(cookie_file: str, region: str = DEFAULT_REGION) -> bool:
    console = Console()
    try:
        from playwright.async_api import async_playwright
    except ImportError:
        if getattr(sys, "frozen", False):
            console.log(
                "[bold red][ERR][/] Auto capture is not available in the prebuilt binary. "
                "Run from source (uv sync --extra capture) or use manual cookie capture. / "
                "预构建二进制不支持自动获取，请使用源码运行或手动获取 Cookie。"
            )
        else:
            console.log(
                "[bold red][ERR][/] Playwright is not installed. Run: uv sync --extra capture"
            )
        return False

    profile_dir = Path(".cookie-browser-profile").resolve()
    profile_dir.mkdir(exist_ok=True)

    loop = asyncio.get_running_loop()
    captured = loop.create_future()

    async with async_playwright() as playwright:
        context = None
        last_error = None
        for channel in ["msedge", "chrome"]:
            try:
                context = await playwright.chromium.launch_persistent_context(
                    str(profile_dir),
                    channel=channel,
                    headless=False,
                    viewport={"width": 1280, "height": 900},
                )
                break
            except Exception as e:
                last_error = e

        if context is None:
            try:
                context = await playwright.chromium.launch_persistent_context(
                    str(profile_dir),
                    headless=False,
                    viewport={"width": 1280, "height": 900},
                )
            except Exception:
                console.log(
                    "[bold red][ERR][/] Could not start a browser. Install Microsoft Edge or run: uv run playwright install chromium"
                )
                if last_error:
                    console.log(f"[dim]{last_error}[/]")
                return False

        page = context.pages[0] if context.pages else await context.new_page()

        def on_request(request):
            if HIDEMYEMAIL_APP_PATH not in request.url:
                return
            if "rootDomain=www" not in request.url:
                return

            cookie = request.headers.get("cookie", "")
            if cookie and not captured.done():
                captured.set_result((request.url, cookie))

        context.on("request", on_request)

        console.print(
            "A browser window opened. Log in if needed; the tool will click Hide My Email and capture the request. / 已打开浏览器；如需登录请先登录，工具会点击隐藏邮件地址并捕获请求。"
        )
        await page.goto(COOKIE_CAPTURE_URLS[region], wait_until="domcontentloaded")

        clicked = False
        deadline = loop.time() + 300
        selectors = [
            'button[aria-label*="隐藏邮件地址"]',
            'button[aria-label*="Hide My Email"]',
            '[data-testid="hidemyemail-app-icon"]',
            "text=隐藏邮件地址",
            "text=Hide My Email",
        ]

        while not captured.done() and loop.time() < deadline:
            if not clicked:
                for selector in selectors:
                    locator = page.locator(selector).first
                    try:
                        if await locator.count():
                            await locator.click(timeout=2000)
                            clicked = True
                            break
                    except Exception:
                        continue
            await asyncio.sleep(1)

        if not captured.done():
            await context.close()
            console.log(
                "[bold red][ERR][/] Timed out waiting for the Hide My Email app request / 等待隐藏邮件地址应用请求超时"
            )
            return False

        request_url, cookie = captured.result()
        account = await fetch_account_info_from_cookie(cookie, region)
        if "error" in account:
            await context.close()
            console.log(f"[bold red][ERR][/] Captured cookie did not validate: {account['error']}")
            return False

        maildomain_host = account.get("detectedMaildomainHost") or ""
        write_captured_cookie(cookie_file, cookie, region, request_url, maildomain_host)
        await context.close()

    console.log(f'[bold green][OK][/] Captured and saved cookie to "{cookie_file}" / 已捕获并保存 Cookie')
    await _whoami(cookie_file, region)
    return True


